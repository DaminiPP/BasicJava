package OOPS;

public class A {

	public A() {
		System.out.println("A Class Default Constructor");
	}
	
	public A(int i) {
		System.out.println("A Class one parameter Constructor" +i);	
	}
	
	public A(int i,int j) {
		System.out.println("A Class two parameter Constructor" +i);	
		System.out.println("A Class two parameter Constructor" +j);	
	}
	
}
