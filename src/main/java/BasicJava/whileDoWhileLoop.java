package BasicJava;

public class whileDoWhileLoop {

	public static void main(String[] args) {

		int i = 1;
		while (i < 10) {
			System.out.println("Printing value of i " + i);
			i++;
		}

		System.out.println("Do while loop----------");

		int j = 18;
		do {
			System.out.println("Print value of j " + j);
			j++;
		} 
		
		while (j < 10);

	}
}
