package BasicJava;

public class DataTypes {

	public static void main(String[] args) {
		
		
		
		
		//primitive data type --->Int,Double,char,boolean;

//	    1: Integer Data Type
//		int--->Data Type Keyword
//		i--->variable name
//		10--->Integer Value Declared
		int i = 10;
		System.out.println(i);
		i = 20;
		System.out.println(i);
// int i=20 --> cannot write this way Duplicate variable is not allowed

	
		
		// 2: Double
		double d = 12.23;
		System.out.println(d);
		double d1 = 152.23;
		System.out.println(d1);
		
		//3: Character --only single digit and single quote
		char c='a';
		char x='A';
		System.out.println(c);
		System.out.println(x);
		
		
		//4: Boolean Data Type
		boolean a1=false;
		boolean b1=true;
		System.out.println(a1);
		System.out.println(b1);
		
		//5: String---> String is a class not a data type ---within double quotes and multiple chars
		
		String s="Damini";
		
		String lastname="Patil";
		System.out.println(s);
		System.out.println(lastname);
	}

}
